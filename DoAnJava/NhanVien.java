    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        try (
            Connection conn = KetNoi.getConnection();
            PreparedStatement pstmt = conn.prepareStatement("DELETE FROM NHANVIEN WHERE MaNV = ?")) {

            // Lấy chỉ số dòng được chọn và kiểm tra
            int selectedRow = tbBang.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng để xóa.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            // Lấy mã phòng từ dòng được chọn
            String MaNV = tbBang.getValueAt(selectedRow, 0).toString().trim();

            // Xác nhận từ người dùng
            int option = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa phòng này." + "'?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
            if (option != JOptionPane.YES_OPTION) {
                return;
            }

            // Thực hiện xóa
            pstmt.setString(1, MaNV);
            int rowsAffected = pstmt.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Xóa phòng thành công!");
                loadNhanVien();
            } else {
                JOptionPane.showMessageDialog(this, "Xóa phòng thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException e) {
            // Xử lý ngoại lệ
            JOptionPane.showMessageDialog(this, "Lỗi khi xóa phòng: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnXoaActionPerformed

