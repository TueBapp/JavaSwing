    private void txtTimCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtTimCaretUpdate
        // TODO add your handling code here:
        try {
            // Get the search query from the input field
            String search = txtTim.getText().trim();

            // Get database connection
            Connection conn = KetNoi.getConnection();

            // Create SQL statement
            String sql = "SELECT * FROM NHANVIEN WHERE MaNV LIKE ? OR Hoten LIKE ? OR Ngsinh LIKE ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);

            // Set search parameters
            pstmt.setString(1, "%" + search + "%");
            pstmt.setString(2, "%" + search + "%");
            pstmt.setString(3, "%" + search + "%");
            // Execute the SQL statement
            ResultSet rs = pstmt.executeQuery();

            // Populate the table with search results
            DefaultTableModel dfModel = (DefaultTableModel) tbBang.getModel();
            dfModel.setRowCount(0); // Clear previous data
            while (rs.next()) {
                dfModel.addRow(new Object[]{
                    rs.getString("MaNV"),
                    rs.getString("Hoten"),
                    rs.getString("Ngsinh"),
                    rs.getString("DChi"),
                    rs.getString("SDT")
                });
            }

            // Close resources
            rs.close();
            pstmt.close();
            conn.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_txtTimCaretUpdate

