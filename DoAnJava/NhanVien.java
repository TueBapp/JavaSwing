    private void loadNhanVien(){
        
    try {
        Connection conn = KetNoi.getConnection();
        Statement stmt = conn.createStatement();
        DefaultTableModel dfModel = (DefaultTableModel) tbBang.getModel();  
        
        // Load data
        ResultSet rs = stmt.executeQuery("SELECT MaNV, Hoten, Ngsinh, DChi, SDT FROM NHANVIEN");
        dfModel.setRowCount(0);
        while (rs.next()) {
            dfModel.addRow(new Object[]{
                rs.getString("MaNV"), 
                rs.getString("Hoten"), 
                rs.getString("Ngsinh"),
                rs.getString("DChi"),
                rs.getString("SDT")
                });
        }
        conn.close();
    } catch (Exception e) {
        e.printStackTrace();
}
}
    

