    private void loadBang(){
        
    try {
        Connection conn = KetNoi.getConnection();
        Statement stmt = conn.createStatement();
        DefaultTableModel dfModel = (DefaultTableModel) tbBang.getModel();  
        // Load data
        ResultSet rs = stmt.executeQuery("SELECT Masach, Tensach, TenTG, MaTL, SL, DGia FROM SACH");
        dfModel.setRowCount(0);
         
        while (rs.next()) {
            dfModel.addRow(new Object[]{
                rs.getString("Masach"), 
                rs.getString("Tensach"), 
                rs.getString("TenTG"),
                rs.getString("MaTL"),
                rs.getString("SL"),
                rs.getString("DGia")
                
            });
             
        } 
        
        conn.close();
    } catch (Exception e) {
        e.printStackTrace();
}
}

