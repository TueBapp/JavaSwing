    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        try (Connection conn = KetNoi.getConnection();
     PreparedStatement pstmt = conn.prepareStatement("UPDATE SACH SET Tensach=?, TenTG=?, MaTL=?, SL=? WHERE  DGia=?")) {

    // Lấy chỉ số dòng được chọn và kiểm tra
    int selectedRow = tbBang.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng để sửa.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        return; 
    }

    // Lấy dữ liệu từ trường nhập liệu và kiểm tra
    String ma = txtMasach.getText().trim();
    String tenSach = txtTensach.getText().trim();
    String tenTG = txtTentg.getText().trim();
    String Tl = txtMatl.getText().trim();
    String soLuong = txtSl.getText().trim();
    String donGia = txtDgia.getText().trim();

    if (tenSach.isEmpty() || tenTG.isEmpty()|| Tl.isEmpty()|| soLuong.isEmpty()|| donGia.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ tên phòng và số điện thoại.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        return; 
    }

    // Xác nhận từ người dùng
    int option = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa thông tin này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
    if (option != JOptionPane.YES_OPTION) {
        return; // Hủy bỏ nếu người dùng chọn Không
    }

    // Điền giá trị vào câu lệnh SQL (lưu ý thứ tự đúng)
    pstmt.setString(1, tenSach);
    pstmt.setString(2, tenTG);
    pstmt.setString(3, Tl);
     pstmt.setString(4, soLuong);
    pstmt.setString(5, donGia);

    // Thực hiện cập nhật
    int rowsAffected = pstmt.executeUpdate();

    if (rowsAffected > 0) {
        JOptionPane.showMessageDialog(this, "Cập nhật phòng thành công!");
        loadBang(); // Cập nhật lại dữ liệu trên bảng
    } else {
        JOptionPane.showMessageDialog(this, "Cập nhật phòng thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
    }

} catch (SQLException e) {
    // Xử lý ngoại lệ
    JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật phòng: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
    e.printStackTrace(); // In ra console để debug (có thể bỏ qua trong sản phẩm cuối cùng)
}
    }//GEN-LAST:event_btnSuaActionPerformed

